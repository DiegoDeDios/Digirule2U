// for PIC18F46K20, erase block size is 64, write block size is 64

void file_load( uint8 slot, bool_t quiet )
{
	uint16 addr;

	EECON1bits.CFGS = 0;		// access flash or EEPROM, not config
	EECON1bits.EEPGD = 1;		// access flash, not EEPROM
	
	for( addr = 0, TBLPTR = (__uint24) &files[slot][0]; addr < 256; addr++, TBLPTR++ )
	{
		if( !quiet )
		{
			address_leds = PROGRESS_BAR( addr );
			sleep_ms( 1 );		// for visual effect only
		}
		
		__asm( "TBLRD" );
		RAM[addr] = IS_REG_ADDR( addr ) ? 0 : TABLAT;
	}
}

void file_save( uint8 slot, bool_t quiet )
{
	uint16 addr;
	
	EECON1bits.CFGS = 0;		// access flash or EEPROM, not config
	EECON1bits.EEPGD = 1;		// access flash, not EEPROM
	
	for( addr = 0, TBLPTR = (__uint24) &files[slot][0]; addr < 256; addr++, TBLPTR++ )
	{
		if( !quiet )
		{
			address_leds = PROGRESS_BAR( addr );
			sleep_ms( 1 );		// for visual effect only
		}
		
		// if at FIRST byte of 64-byte block, erase
		if( (TBLPTRL & 0x3F) == 0 )
		{
			GIE = 0;
			EECON1bits.WREN = 1;
			EECON1bits.FREE = 1;
			EECON2 = 0x55;
			EECON2 = 0xAA;
			EECON1bits.WR = 1;
			EECON1bits.WREN = 0;
			GIE = 1;
		}
		
		// write holding register
		TABLAT = IS_REG_ADDR( addr ) ? 0 : RAM[addr];
		__asm( "TBLWT" );
		
		// if at LAST byte of 64-byte block, write
		if( (TBLPTRL & 0x3F) == 0x3F )
		{
			GIE = 0;
			EECON1bits.WREN = 1;
			EECON2 = 0x55;
			EECON2 = 0xAA;
			EECON1bits.WR = 1;
			EECON1bits.WREN = 0;
			GIE = 1;
		}
	}
}
